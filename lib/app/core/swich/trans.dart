// Kamus terjemahan UI aplikasi, berbasis KEY (bukan berbasis kalimat
// hardcoded). Ini yang membuat SELURUH teks/label/tombol/pesan di
// aplikasi otomatis berganti mengikuti bahasa yang dipilih di
// Pengaturan (lihat LanguageProvider).
//
// PENTING — batasan yang WAJIB dijaga di seluruh aplikasi:
//   • Kamus ini HANYA untuk teks/label statis milik aplikasi (judul,
//     label field, tombol, pesan error/sukses, dll).
//   • Kamus ini TIDAK PERNAH dipakai untuk menampilkan data otentik
//     milik pengguna seperti nama, email, UID, No. WhatsApp, alamat,
//     tanggal lahir, dsb. Data-data itu diambil apa adanya dari
//     `UserModel` dan ditampilkan langsung (lihat AccountInfoPage) —
//     TIDAK diterjemahkan / TIDAK dilewatkan ke `tr()`.
//
// Cara pakai di widget (lihat ekstensi `L10nX` di l10n.dart):
//   Text(context.tr('settings_title'))
//   Text(context.tr('toggle_enabled', params: {'label': bioProvider.label}))
//
// CATATAN PERBAIKAN:
//   Sebelumnya hanya 'id' dan 'en' yang terisi, sehingga bahasa lain
//   (ms, th, fil, vi, km, pt, ja, ko, zh, zh-TW, hi, ar, de, nl, fr)
//   selalu fallback ke 'en' — akibatnya UI aplikasi tetap berbahasa
//   Inggris walau pengguna memilih bahasa lain di Pengaturan. Semua
//   bahasa yang terdaftar di `kSupportedLanguages` (lihat
//   core/data/languages.dart) sekarang sudah diisi penuh di bawah.
//
// Menambah bahasa baru:
//   1. Tambahkan Map baru di `_translations` dengan key bahasa yang
//      sama dengan `LanguageInfo.code` (lihat languages.dart).
//   2. Isi sebanyak mungkin key. Key yang belum diisi otomatis
//      fallback ke 'en', lalu ke key itu sendiri (tidak pernah blank).
//
// Menambah string baru:
//   1. Tambahkan key baru ke minimal `id` dan `en`.
//   2. Pakai `context.tr('key_baru')` di widget.
// ─────────────────────────────────────────────

