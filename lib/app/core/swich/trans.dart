  /// Ambil terjemahan untuk [key] pada bahasa [languageCode].
  /// Urutan fallback: bahasa aktif → 'en' → key itu sendiri (supaya
  /// tidak pernah menampilkan string kosong / null / crash walau ada
  /// key yang belum diterjemahkan untuk bahasa tertentu).
  static String t(String languageCode, String key, {Map<String, String>? params}) {
    final byLang = _translations[languageCode];
    String? raw = byLang?[key];
    raw ??= _translations[fallbackCode]?[key];
    raw ??= key;

    if (params == null || params.isEmpty) return raw;

    var result = raw;
    for (final entry in params.entries) {
      result = result.replaceAll('{${entry.key}}', entry.value);
    }
    return result;
  }

  /// Bahasa yang penulisannya berjalan kanan-ke-kiri (RTL).
  /// Dipakai untuk menentukan `Directionality` di MaterialApp.
  static const Set<String> rtlLanguageCodes = {'ar'};

  static bool isRtl(String languageCode) => rtlLanguageCodes.contains(languageCode);
